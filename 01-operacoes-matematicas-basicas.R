# Usando o R como calculadora:

# Adição:
1 + 1 

# Subtração:
1 - 1

# Multiplicação

1 * 2

# DIvisão:

2 / 1

# Potência:
2 ^ 2 

# Raíz quadrada:
sqrt(2)

# Logaritmo:
log(10) # Por default, vem na base 10
log(10, base = 2) # Aqui estamos passando uma base

# Parenteses:
(1 + 1) * 3

# Objetos: há duas formas
numero = 10
numero <- 12

# Operações com objetos:
numero * 3

# Descobrir o tipo de dado:
str(numero) # ele diz que é do tipo numérico














